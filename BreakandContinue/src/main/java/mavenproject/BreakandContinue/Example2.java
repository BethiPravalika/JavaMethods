package mavenproject.BreakandContinue;

public class Example2 {

	public static void main(String[] args) {

		for(int i = 1; i <= 10; i++)
	       {
	    	   if(i == 7)
	    	   {
	    		  continue; //continue statement
	    	   }
	    	   
	    	   System.out.println(i);
	       }
	}

}
